public class OperatorAssigment {
	public static void main(String[] args) {
		int x = 1;
		System.out.println(x);
		System.out.println(x += 1);
		System.out.println(x -= 1);
		System.out.println(x *= 1);
		System.out.println(x /= 1);
		System.out.println(x %= 1);

		x = 10;
		System.out.println((x == 11)?11:10);

	}
	
}